<p class="subtitleinfor">  
Finalità dei trattamenti e base giuridica
</p>
<p class="infor"> 
Nel rispetto dei principi di liceità, correttezza, trasparenza, adeguatezza, pertinenza e necessità di cui all'art. 5, paragrafo 1 del GDPR l'Università, 
in qualità di Titolare del trattamento, provvederà al trattamento dei dati personali da Lei forniti per la stipula del contratto di insegnamento, 
esclusivamente per permettere lo svolgimento della stessa nel rispetto della normativa vigente in materia. In particolare i dati da lei forniti saranno 
raccolti e trattati, con modalità manuale, cartacea e informatica, mediante il loro inserimento in archivi cartacei e/o informatici 
per il perseguimento delle seguenti finalità:<br>
1. gestione del rapporto di lavoro instaurato con l'Ateneo;<br>
2. assolvimento di obblighi o per finalità correlate al rapporto contrattuale;<br>
3. gestione dell'anagrafica;<br>
4. invio di comunicazioni inerenti il proprio rapporto contrattuale;<br>
5. gestione delle pratiche assicurative, previdenziali e fiscali, trattamenti assistenziali, eventuali denunce pratiche di infortunio;<br>
6. trattamento dei dati nei giudizi pendenti di fronte a tutte le giurisdizioni che coinvolgono il collaboratore;<br>
7. erogazione del compenso;<br>
8. gestione dei provvedimenti per i collaboratori;<br>
9. utilizzo dei servizi bibliotecari, anche digitali;<br>
10. accesso ai laboratori e ad altre strutture;<br>
11. applicazione delle misure di sicurezza degli ambienti di lavoro secondo le disposizioni previste dal D. Lgs. 81/2008;<br>
12. eventuale utilizzo di agevolazioni inerenti la disabilità cronica o temporanea dell'interessato;<br>
13. statistiche e ricerca storica e scientifica, in forma anonima.<br>
Inoltre i dati personali identificativi e di rapporto contrattuale saranno trattati per le seguenti finalità:<br>
- esecuzione dei servizi offerti agli utenti attraverso i sistemi informatici dell'Università;<br>
- comunicazione a soggetti esterni pubblici o privati per fini occupazionali o culturali.<br>
Il titolare non utilizza processi automatici finalizzati alla profilazione dell'interessato.<br>
</p>
<br>
<p class="subtitleinfor">  
Categorie di destinatari dei dati ed eventuale trasferimento dei dati
</p>
<p class="infor">  
I dati personali trattati per le finalità di cui sopra verranno comunicati o saranno comunque accessibili ai dipendenti e collaboratori 
assegnati ai competenti uffici dell'Università, che, nella loro qualità di referenti per la protezione dei dati e/o amministratori di 
sistema e/o incaricati del trattamento, saranno a tal fine adeguatamente istruiti dal Titolare.<br>
L'Università può comunicare i dati personali di cui è titolare anche ad altre amministrazioni pubbliche qualora queste debbano trattare i 
medesimi per eventuali procedimenti di propria competenza istituzionale nonché a tutti quei soggetti pubblici ai quali, in presenza dei relativi 
presupposti, la comunicazione è prevista obbligatoriamente da disposizioni comunitarie, norme di legge o regolamento.<br>
La gestione e la conservazione dei dati personali raccolti dall'Università avviene su server ubicati all'interno dell'Università e/o su 
server esterni di fornitori di alcuni servizi necessari alla gestione tecnico-amministrativa che, ai soli fini della prestazione richiesta, 
potrebbero venire a conoscenza dei dati personali degli interessati e che saranno debitamente nominati come Responsabili del trattamento a norma 
dell'art. 28 del GDPR. 
</p>
<br>
<p class="subtitleinfor">  
Periodo di conservazione dei dati
</p>
<p class="infor">  
Nella Sua qualità di interessato al trattamento, Lei ha diritto di richiedere all'Università, quale Titolare del trattamento, ai sensi degli artt.15, 16, 17, 18, 19 e 21 del GDPR:
- l'accesso ai propri dati personali ed a tutte le informazioni di cui all'art.15 del GDPR:<br>
- la rettifica dei propri dati personali inesatti e l'integrazione di quelli incompleti;<br>
- la cancellazione dei propri dati, fatta eccezione per quelli contenuti in atti che devono essere obbligatoriamente conservati 
dall'Università e salvo che sussista un motivo legittimo prevalente per procedere al trattamento;<br>
- la limitazione del trattamento ove ricorra una delle ipotesi di cui all'art.18 del GDPR.<br>
Ha altresì diritto:<br>
- di opporsi al trattamento dei propri dati personali, fermo quanto previsto con riguardo alla necessità ed obbligatorietà del 
trattamento ai fini dell'instaurazione del rapporto;<br>
- di revocare il consenso eventualmente prestato per i trattamenti non obbligatori dei dati, senza con ciò pregiudicare la liceità 
del trattamento basata sul consenso prestato prima della revoca.<br>
</p>
